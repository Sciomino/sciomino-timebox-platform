<p>
<b>To the seekers of truth</b> the Buddha says: 
</p>
<p>
"<br/>
Do not accept anything on (mere) hearsay <i>-- (i.e., thinking that thus have we heard it for a long time)</i>.<br/>
Do not accept anything by mere tradition <i>-- (i.e., thinking that it has thus been handed down through many generations)</i>.<br/>
Do not accept anything on account of mere rumors <i>-- (i.e., by believing what others say without any investigation)</i>.<br/>
Do not accept anything just because it accords with your scriptures.<br/>
Do not accept anything by mere suppositions.<br/>
Do not accept anything by mere inference.<br/>
Do not accept anything by merely considering the reasons.<br/>
Do not accept anything merely because it agrees with your pre-conceived notions.<br/>
Do not accept anything merely because it seems acceptable <i>-- (i.e., thinking that as the speaker seems to be a good person his words should be accepted)</i>.<br/>
Do not accept anything thinking that the ascetic is respected by us <i>-- (ie., therefore it is right to accept his word).</i><br/>
</p>
<p>
But when you know for yourselves -- these things are immoral, these things are blameworthy, these things are censured by the wise, these things, when performed and undertaken conduce to ruin and sorrow -- then indeed do you reject them. 
</p>
<p>
When you know for yourselves -- these things are moral, these things are blameless, these things are praised by the wise, these things, when performed and undertaken, conduce to well-being and happiness -- then do you live acting accordingly.<br/>
" 
</p>
<p>
<i>
--<br/>
comments by Narada Thera
</i>
